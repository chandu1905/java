//18,19 WAP TO PRINT ALPHABETS FROM a TO z and A to Z
class aTOz 
{
	public static void main(String[] args) 
	{
		System.out.println("the alphabets from a to z are: ");
		for(char ch='a';ch<='z';ch++)
			{
			System.out.println(ch);
			}
			System.out.println("the alphabets from A to Z are: ");
			for(char ch='A';ch<='Z';ch++)
			{
			System.out.println(ch);
			}

	}
}
