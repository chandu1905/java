class Swap1{
public static void main(String[] args){
int i=3,j=4;
System.out.println("value of i before swapping is i="+i);
System.out.println("value of j before swapping is j="+j);
i=i+j; //i=3+4=7
j=i-j; //j=7-4=3
i=i-j; //i=7-3=4
System.out.println("value of i after swapping is i="+i);
System.out.println("value of j after swapping is j="+j);

}
}

